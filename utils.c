#include"pipex.h"

int ft_strncmpp(char *s1, char *s2, int n)
{
    int i;

    i = 0;
    if(s1 || s2)
    {
        while(s1 && s1[i] && s2 && s2[i] && i < n)
        {
            if(s1[i] != s2[i])
            return (0);
            i++;
        }
        return (1);
    }
    return (0);
}